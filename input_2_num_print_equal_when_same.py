# Prog02: Create a program that ask user to input 2 numbers. Print "Equal" when the numbers are the same.

# ask user for 2 numbers
num1 = int(input('Enter 1st number: '))
num2 = int(input('Enter 2nd number: '))

# print equal if same
if num1 == num2:
    print('Equal!')
else:
    print('Not equal!')
    

